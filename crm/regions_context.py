def get_context(region: str) -> dict:
    """Returns the context for a region, falling back to generic."""
    if region in REGION_CONTEXT:
        return REGION_CONTEXT[region]
    # Try case-insensitive partial match
    rl = (region or "").lower().strip()
    for key, ctx in REGION_CONTEXT.items():
        if key.lower() == rl:
            return ctx
    return GENERIC_CONTEXT
